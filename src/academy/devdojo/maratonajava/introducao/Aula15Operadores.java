package academy.devdojo.maratonajava.introducao;

// + - / *
public class Aula15Operadores {
    public static void main(String[] args) {

        int numero01 = 10;
        int numero02 = 20;

        System.out.println(numero02+numero01);
        System.out.println(numero02-numero01);
        System.out.println(numero02/numero01);
        System.out.println(numero02*numero01);
    }

}
